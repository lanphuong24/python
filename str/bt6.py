## Thao tác cơ bản trên xâu ký tự

s = "tin học python"

# Chuyển xâu s thành chữ hoa toàn bộ
# Chuyển xâu s thành chữ thường toàn bộ
# Chuyển xâu s thành dạng viết hoa chữ cái đầu mỗi từ
# Code phải đảm bảo vẫn đúng khi sửa đổi xâu s

upper = s.upper()
lower = s.lower()
capitalized = s.title()

print("Chữ hoa:", upper)
print("Chữ thường:", lower)
print("Viết hoa đầu mỗi từ:", capitalized)
